package simpleprograminjava;

public class Operato {
public static void main(String[]args) {
	int x =95;
	int y = 82;
	
	System.out.println(x+y);
	System.out.println(x-y);
	System.out.println(x*y);
	System.out.println(x/y);
	System.out.println(x%y);
	
	
	
	int a =23;
	a+=21;
	System.out.println(a);
	
	int b =50;
	b*=2;
	System.out.println(b);
	
	
	
	int s =36;
	int c =36;
    System.out.println(s==c);
    System.out.println(s!=c);
    System.out.println(s<c);
    System.out.println(s>c);
    System.out.println(s<=c);
    System.out.println(s>=c);
}
}
